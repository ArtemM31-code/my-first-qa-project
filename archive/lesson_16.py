class User:
     # Конструктор пользователя
     def __init__(self, username, role):
         self.username = username
         self.role = role
#         
#         
# 
#     # Метод входа в систему
     def login(self):
         print("В систему зашел", self.username)
         print("Его роль:", self.role)

