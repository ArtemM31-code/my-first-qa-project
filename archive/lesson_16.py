# Урок 6.1: Объектно-Ориентированное Программирование (ООП) - Подробный разбор

# === ЧАСТЬ 1: ЧТО ТАКОЕ ООП И ЗАЧЕМ ОНО НУЖНО? ===
# До сих пор мы писали программы как "список инструкций" (сделай А, потом Б, вызови функцию В).
# Это отлично работает для маленьких скриптов. Но когда код разрастается (например, в автотестах
# появляются десятки страниц сайта), управлять отдельными функциями становится невероятно сложно.
#
# ООП — это другой способ мышления. Мы представляем программу не как набор команд, 
# а как набор ОБЪЕКТОВ, которые общаются друг с другом.
# У любого объекта в реальном мире есть:
# 1. СВОЙСТВА (какой он? из чего состоит?) -> в Питоне это называется АТРИБУТЫ (или переменные объекта).
# 2. ДЕЙСТВИЯ (что он умеет делать?) -> в Питоне это называется МЕТОДЫ (или функции объекта).

# === ЧАСТЬ 2: КЛАСС И ОБЪЕКТ ===
# КЛАСС — это чертеж или техническое задание. 
# На чертеже автомобиля написано, что у него должны быть колеса, двигатель и он должен уметь ездить. 
# Но на самом чертеже нельзя поехать в магазин!
# 
# ОБЪЕКТ (или экземпляр класса) — это реальная машина, собранная на заводе по этому чертежу. 
# Машин может быть миллион. Они все собраны по одному чертежу (Классу Car), но у одной машины 
# цвет красный, а у другой синий, у одной бензина полно, а у другой бак пуст.

# Давай посмотрим на код чертежа (класса) для автомобиля:
class Car:
    
    # Метод __init__ (инициализатор / конструктор) — это главная магия создания объекта.
    # Эта функция АВТОМАТИЧЕСКИ запускается в ту секунду, когда мы "собираем" новую машину.
    # Слово `self` (переводится как "сам") означает: "мы обращаемся именно к той конкретной машине, 
    # которую сейчас собираем или используем".
    def __init__(self, make, color):
        # Прикрепляем свойства (атрибуты) к конкретному автомобилю:
        self.brand = make       # Создали свойство "марка" и положили в него переданное значение
        self.car_color = color  # Создали свойство "цвет"
        self.engine_on = False  # Свойство "двигатель заведен". По умолчанию у новой машины он выключен.

    # Это МЕТОД (действие). Обычная функция, но она "живет" внутри класса.
    # Метод внутри класса ВСЕГДА должен принимать `self` первым аргументом. Так метод понимает, 
    # с какой именно машиной он сейчас работает.
    def start_engine(self):
        self.engine_on = True
        print(f"Двигатель автомобиля {self.brand} успешно заведен! Врррр!")

    def show_info(self):
        print(f"Это {self.car_color} автомобиль марки {self.brand}.")


# === ЧАСТЬ 3: РАБОТА С ОБЪЕКТАМИ ===
# Теперь мы можем создавать реальные объекты по нашему чертежу!
# Раскомментируй блок ниже (выдели строки с 49 по 56 и нажми Ctrl+/) и запусти файл:

my_car = Car("Toyota", "Красный") # Вызывается __init__, создается красная Тойота
friend_car = Car("BMW", "Черный") # Вызывается __init__, создается черная БМВ

my_car.show_info()     # Тойота рассказывает о себе
friend_car.show_info() # БМВ рассказывает о себе

my_car.start_engine()  # Заводим только Тойоту. БМВ всё еще заглушена!


# === ЧАСТЬ 4: КАК ЭТО ПОМОЖЕТ В ТЕСТИРОВАНИИ? ===
# В автотестировании (особенно в тестах сайтов) очень популярен подход Page Object Model (POM).
# Суть в том, что каждая страница сайта в твоем коде — это отдельный Класс!
#
# Свойства страницы (атрибуты) — это ее URL и адреса кнопок (локаторы).
# Действия страницы (методы) — "нажать на кнопку входа", "ввести пароль", "прочитать заголовок".
# И в самих тестах мы просто создаем объект нужной страницы и вызываем его методы. 
# Это делает код тестов невероятно читаемым, понятным и его легко поддерживать!


