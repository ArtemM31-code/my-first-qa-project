def greet_user(name):
     print("Добро пожаловать,", name)

greet_user("Артём")
greet_user("Анна")

# 3. ВОЗВРАТ ЗНАЧЕНИЯ (return)
# Часто функция должна не просто напечатать текст на экран, а сделать вычисление и ОТДАТЬ результат обратно.
# Для этого используется слово `return`.

def calculate_discount(price, discount_percent):
    discount_amount = price * (discount_percent / 100)
    final_price = price - discount_amount
    return final_price

# # Сохраняем результат работы функции в переменную
result = calculate_discount(1000, 20)
print("Цена со скидкой:", result)


