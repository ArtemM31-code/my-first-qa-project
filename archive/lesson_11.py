# 1. СОЗДАНИЕ ФУНКЦИИ
# Функция создается с помощью слова `def` (от define - определить).
# Раскомментируй и запусти код:

def say_hello():
    print("Привет! Я функция-помощник.")
    print("Я выполняюсь только тогда, когда меня вызовут.")

# # Если запустить программу сейчас, ничего не произойдет! Функцию нужно ВЫЗВАТЬ по имени:
say_hello()
say_hello() # Можно вызывать сколько угодно раз

# 2. АРГУМЕНТЫ ФУНКЦИИ
