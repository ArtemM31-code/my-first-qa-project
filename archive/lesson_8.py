# Урок 3.2: Кортежи (Tuples)

# Кортеж (tuple) — это младший брат списка. Он выглядит и работает почти так же, но с одним ГЛАВНЫМ отличием:
# ЕГО НЕЛЬЗЯ ИЗМЕНЯТЬ.
# Если список — это коробка, куда можно докладывать вещи и вынимать их, то кортеж — это запаянный сейф.

# 1. СОЗДАНИЕ КОРТЕЖА
# Кортежи создаются с помощью круглых скобок ( ) вместо квадратных [ ].
# Раскомментируй код:
#my_tuple = ("яблоко", "банан", "киви")
#print("Мой кортеж:", my_tuple)

# 2. ПОЛУЧЕНИЕ ЭЛЕМЕНТОВ
