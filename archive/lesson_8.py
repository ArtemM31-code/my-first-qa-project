#my_tuple[0] = "груша"        # Ошибка! Нельзя заменить элемент.

# ЗАЧЕМ НУЖНЫ КОРТЕЖИ?
# 1. Они работают чуть быстрее списков.
# 2. Они защищают данные от случайного изменения. Если у тебя есть список настроек сайта (например, ширина и высота экрана), 
#    лучше хранить их в кортеже, чтобы никто случайно их не переписал.
# 3. Их можно использовать как ключи в словарях (мы пройдем словари в следующем уроке).

# === ТВОЁ ЗАДАНИЕ ===
# В тестировании часто используются кортежи для хранения разрешений экранов, на которых нужно прогнать тесты.
#
# 1. Создай кортеж `screen_resolutions` и положи в него три строковых значения: "1920x1080", "1366x768", "375x667".
#    (Не забудь использовать круглые скобки!)
# 2. С помощью цикла `for` перебери этот кортеж и выведи на экран: "Запускаем тест на разрешении: [разрешение]"

# Пиши код ниже этой строки:
screen_resolutions = ("1920x1080", "1366x768", "375x667")
for screen in screen_resolutions:
    print("Запускаем тест на разрешении:", screen)
