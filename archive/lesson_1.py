# Урок 1: Введение, вывод на экран и базовая математика

# 1. Функция print() используется для вывода текста или данных на экран.
print("Привет, мир!")
print("Я начинаю изучать Python!")

# 2. Математические операции
# Python умеет работать как калькулятор.
print("2 + 2 =", 2 + 2)          # Сложение
print("10 - 3 =", 10 - 3)        # Вычитание
print("4 * 5 =", 4 * 5)          # Умножение
print("20 / 4 =", 20 / 4)        # Деление (всегда возвращает дробное число)
print("10 // 3 =", 10 // 3)      # Целочисленное деление (без остатка)
print("10 % 3 =", 10 % 3)        # Остаток от деления
print("2 ** 3 =", 2 ** 3)        # Возведение в степень (2 в кубе)

