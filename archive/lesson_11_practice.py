# 1. Мы ОДИН РАЗ пишем шаблон поздравления и называем его `congratulate`
# Слово `name` в скобках — это "заглушка". Сюда потом попадет реальное имя.
def congratulate(name):
    print("С днем рождения,", name, "!")
    print("Желаем счастья и здоровья!")
    print("Твой подарок уже в пути.")
    print()

# 2. А теперь мы просто используем этот шаблон, передавая в него разные имена!
# Смотри, как сократился код вызова:
congratulate("Артём")
congratulate("Анна")
congratulate("Иван")

