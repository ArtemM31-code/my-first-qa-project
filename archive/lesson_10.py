# Урок 3.4: Множества (Sets)

# Представь, что у тебя есть список email-адресов пользователей, но некоторые адреса случайно задублировались.
# Как быстро удалить все дубликаты? Писать цикл? Нет! В Python для этого есть Множества (Sets).

# Множество — это мешок с вещами, в котором:
# 1. Нет никакого порядка (у элементов нет индексов 0, 1, 2).
# 2. ВСЕ ЭЛЕМЕНТЫ УНИКАЛЬНЫ. Одинаковых значений там быть не может физически.

# 1. СОЗДАНИЕ МНОЖЕСТВА И УДАЛЕНИЕ ДУБЛИКАТОВ
# Раскомментируй код:
emails_list = ["test@mail.ru", "admin@mail.ru", "test@mail.ru", "user@mail.ru"]
print("Список с дубликатами:", emails_list)

# # Превращаем список в множество с помощью функции set()
unique_emails = set(emails_list)
print("Уникальные email (множество):", unique_emails)

# 2. ОПЕРАЦИИ С МНОЖЕСТВАМИ
# Множества поддерживают математические операции. Например, поиск пересечений (что есть и там, и там).
# Раскомментируй:
front_end_bugs = {"bug1", "bug2", "bug3"}
back_end_bugs = {"bug3", "bug4", "bug5"}

# # Пересечение (знак &)
common_bugs = front_end_bugs & back_end_bugs
print("Общие баги для фронта и бэка:", common_bugs)


