# Урок 8.1: Твой первый настоящий автотест на Pytest!

# Мы добрались до самого главного — Pytest!
# Pytest — это самый популярный фреймворк для тестирования на Python.
# Он автоматически ищет файлы и функции, которые начинаются со слова `test_`, и запускает их.

# В Pytest мы проверяем результат с помощью одного-единственного слова: `assert` (утверждаю).
# Если после `assert` стоит правда (True), тест зеленый (PASSED). 
# Если ложь (False), тест падает (FAILED).

# Давай напишем простую функцию (представим, что ее написал разработчик):
def calculate_discount(price, discount):
    return price - discount

# === ТЕСТЫ ===
# Функция теста ВСЕГДА должна начинаться с test_
def test_discount_math():
    # 1. Вызываем функцию разработчика
    result = calculate_discount(100, 20)
    
    # 2. Проверяем результат (Утверждаю, что результат равен 80)
    assert result == 80

def test_string_length():
    password = "admin"
    # Утверждаю, что длина пароля равна 5 символам
    assert len(password) == 5

