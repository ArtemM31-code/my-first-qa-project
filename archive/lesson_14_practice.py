# ПРАКТИКА: Работа с файлами (w, r, a)
# Давай закрепим работу с файлами, чтобы ты набивал руку и перестал их бояться!

# --- ЗАДАЧА 1: Режим записи "w" ---
# 1. Открой (или создай) файл "config.txt" в режиме "w".
# 2. Запиши в него строку: "version = 1.0\n"
# (Питон сам закроет файл, когда блок закончится)
print("--- ЗАДАЧА 1 ---")
# Пиши код ниже:
with open("config.txt", "w", encoding="utf-8") as file:
    file.write("version = 1.0\n")
print("Файл успешно создан и записан!")



# --- ЗАДАЧА 2: Режим чтения "r" ---
# 1. Открой этот же файл "config.txt" в режиме "r".
# 2. Прочитай его содержимое в переменную `text` (используй file.read()).
# 3. Распечатай переменную `text` на экран (в терминал) с помощью print().
print("\n--- ЗАДАЧА 2 ---")
# Пиши код ниже:
with open("config.txt", "r", encoding="utf-8") as file:
    text = file.read()
    print(text)



# --- ЗАДАЧА 3: Режим добавления "a" ---
# 1. Снова открой "config.txt", но уже в режиме "a".
# 2. ДОПИШИ в конец файла строку: "author = QA Engineer\n".
# 3. Чтобы убедиться, что всё сработало, после этого блока напиши обычный `print("Всё готово!")`.
# 4. Открой файл `config.txt` глазками в левом меню PyCharm и посмотри, есть ли там две строчки!
print("\n--- ЗАДАЧА 3 ---")
# Пиши код ниже:
with open("config.txt", "a", encoding="utf-8") as file:
    file.write("author = QA Engineer\n")
print("Всё готово!")


