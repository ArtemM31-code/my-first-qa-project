from playwright.sync_api import sync_playwright

# Ты был абсолютно прав! До этого мы тестировали "игрушечные" данные в памяти компьютера.
# Настоящие QA-инженеры тестируют реальные сайты в реальных браузерах.
# Для этого мы будем использовать Playwright — самый современный инструмент автоматизации (круче Selenium).

# Этот тест РЕАЛЬНО откроет браузер у тебя на экране, сам зайдет на Википедию, 
# введет текст, нажмет Enter и проверит результат.

def test_wikipedia_search():
    # 1. Запускаем "кукловода" (Playwright)
    with sync_playwright() as p:
        # 2. Открываем настоящий браузер (headless=False значит "покажи мне окно на экране!")
        # slow_mo=1000 специально замедляет робота, чтобы ты успел увидеть, как он печатает
        browser = p.chromium.launch(headless=False, slow_mo=1000) 
        
        # 3. Открываем новую вкладку
        page = browser.new_page()
        
        # 4. Переходим на сайт
        print("\n[РОБОТ] Заходим на Википедию...")
        page.goto("https://ru.wikipedia.org/")
        
        # 5. Ищем строку поиска и печатаем слово "Python"
        print("[РОБОТ] Вводим 'Python' в поиск...")
        page.fill("input[name='search']", "Python")
        
        # 6. Нажимаем Enter на клавиатуре
        print("[РОБОТ] Нажимаем Enter...")
        page.press("input[name='search']", "Enter")
        
        # 7. Получаем заголовок загрузившейся страницы
        page.wait_for_load_state("load")
        title = page.title()
        print(f"[РОБОТ] Заголовок страницы: {title}")
        
        # 8. НАШ ГЛАВНЫЙ ASSERT! Проверяем, что статья действительно про Python
        assert "Python" in title
        
        # 9. Закрываем браузер
        print("[РОБОТ] Тест пройден! Закрываем браузер.")
        browser.close()

