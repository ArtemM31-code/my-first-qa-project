# Урок 8.2: Мощный `assert` в реальных боевых условиях

# В прошлом уроке мы проверяли самое простое: равенство (==) и больше/меньше (>).
# Но в автотестировании сайтов или API мы чаще всего ищем КУСОК текста или 
# проверяем списки товаров.

# Давай посмотрим на три самых частых проверки (assert) в работе QA:

# 1. Проверка вхождения текста (Оператор `in`)
# Представь, что мы получили текст с сайта. Нам нужно убедиться, 
# что там есть слово "Заблокирован".
def test_error_message():
    site_text = "Ваш аккаунт Заблокирован за спам!"
    # Утверждаю, что слово "Заблокирован" находится ВНУТРИ текста
    assert "Заблокирован" in site_text

# 2. Проверка длины списка (Функция `len()`)
# Представь, что мы положили в корзину 3 товара. Нужно убедиться, что их ровно 3.
def test_cart_items():
    cart = ["Ноутбук", "Мышка", "Коврик"]
    # Утверждаю, что длина (length) списка корзины равна 3
    assert len(cart) == 3

# 3. Несколько assert в одном тесте
# Один тест может делать несколько проверок. Но помни: если упадет первый assert, 
# тест мгновенно остановится, и второй assert даже не запустится.
def test_user_profile():
    user_api_response = {"name": "Artem", "role": "admin"}
    assert user_api_response["name"] == "Artem"
    assert user_api_response["role"] == "admin"


