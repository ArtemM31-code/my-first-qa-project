# Урок 5.2: Обработка ошибок (try / except)

# В программировании ошибки (exceptions) случаются постоянно. 
# Например: мы пытаемся прочитать файл, которого нет. Или делим на ноль. Или сервер упал.
# Если Питон встречает ошибку, он в панике "роняет" всю программу (ты видел этот красный текст в консоли).

# В автотестировании это недопустимо! Если один тест упал из-за ошибки, остальные 100 тестов должны продолжить работу.
# Для этого мы "отлавливаем" ошибки с помощью конструкции try / except.

# 1. КАК ПАДАЕТ ПРОГРАММА
# Раскомментируй код ниже и запусти. Программа сразу умрет из-за деления на ноль.
#print("Этот текст никогда не напечатается!")

# 2. КАК СПАСТИ ПРОГРАММУ
# Мы заворачиваем опасный код в блок `try` (попытайся).
# А если что-то пошло не так, Питон не убивает программу, а переходит в блок `except` (исключение).

# Раскомментируй этот блок и запусти:
try:
     print("Пытаюсь разделить 10 на ноль...")
     result = 10 / 0
except ZeroDivisionError:
     print("Ой! Произошла ошибка: делить на ноль нельзя!")

print("Программа работает дальше! Ура!")


