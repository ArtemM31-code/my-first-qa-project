# Представь, что этот класс (чертеж кнопки на сайте) написали до тебя. 
# Не пытайся его запомнить, просто посмотри, какие 2 свойства он просит при создании 
# и какое действие он умеет делать:
class Button:
    def __init__(self, text, color):
        self.text = text
        self.color = color
        self.is_clicked = False
        
    # Действие: нажать кнопку
    def click(self):
        self.is_clicked = True
        print(f"Кнопка [{self.text}] цвета '{self.color}' была НАЖАТА!")


