# В тестировании нам часто нужен готовый тестовый пользователь.
# 1. Раскомментируй фикстуру `admin_user` ниже (выдели строки и нажми Ctrl+/).
# 2. Напиши тест `def test_admin_access(admin_user):`
# 3. Внутри теста напиши `assert`, который проверяет, что `admin_user["role"]` равен "admin".
# 4. Запусти файл (лучше через терминал командой `pytest -s test_lesson_20_fixtures.py`, 
#    чтобы увидеть наши принты! Флаг `-s` заставляет Pytest показывать принты).

@pytest.fixture
def admin_user():
     print("\n[ПОДГОТОВКА] Создаем тестового пользователя в базе данных...")
     user = {"name": "Artem", "role": "admin"}
     yield user
     print("\n[УБОРКА] Удаляем пользователя из базы данных...")

# Пиши свой тест ниже:
def test_admin_access(admin_user):
    assert admin_user["role"] == "admin"
