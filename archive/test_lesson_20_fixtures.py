# Урок 8.3: Магия Pytest — Фикстуры (Fixtures)

import pytest

# Зачем нужны фикстуры?
# Представь, что у тебя 100 тестов, и перед каждым тестом нужно "открыть браузер" и "залогиниться".
# Писать этот код 100 раз — плохая идея. 
# Фикстура — это "помощник", который сам делает подготовительную работу ДО начала теста, 
# а потом прибирает за собой ПОСЛЕ теста.

# 1. Создаем фикстуру (Обязательно пишем декоратор @pytest.fixture перед функцией)
@pytest.fixture
def browser():
    print("\n[ПОДГОТОВКА] Открываем браузер Chrome...")
    # Представим, что мы создали драйвер браузера
    browser_driver = "Chrome Browser"
    
    # Слово yield означает "Отдай это тесту, а когда тест закончится — продолжи код ниже"
    yield browser_driver 
    
    print("\n[УБОРКА] Закрываем браузер Chrome...")


# 2. Как использовать фикстуру? 
# Просто напиши её имя в скобках у твоего теста! Pytest сам её найдет и запустит.
def test_google_search(browser):
    # В переменной browser теперь лежит то, что отдал yield
    print(f"Тест работает в браузере: {browser}")
    assert "Chrome" in browser


