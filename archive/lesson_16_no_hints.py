# У нас есть готовый класс "Тестовый Отчет". Он ждет только название теста при создании.
class Report:
    def __init__(self, test_name):
        self.name = test_name
        
    def pass_test(self):
        print(f"✅ Тест '{self.name}' ПРОЙДЕН УСПЕШНО!")
        
    def fail_test(self):
        print(f"❌ Тест '{self.name}' УПАЛ С ОШИБКОЙ!")

