# На собеседованиях часто дают задачи, приближенные к реальной работе тестировщика.
# Представь: наш автотест проверил 6 страниц сайта и собрал их HTTP-статусы.
# Успешный статус — это 200. Всё, что не 200 (например, 404 или 500) — это ошибки.
#
# ТВОЯ ЛОГИЧЕСКАЯ ЗАДАЧА:
# Посчитать, сколько сломанных страниц (ошибок) нашел наш автотест.
#
# Список статусов, которые вернул сайт:
statuses = [200, 404, 200, 500, 200, 401]
#
# Твой план действий (включи логику!):
# 1. Создай переменную-счетчик для ошибок (например, `errors_count = 0`).
# 2. Напиши цикл `for`, чтобы по очереди проверить каждый статус в списке `statuses`.
# 3. Напиши условие `if`: если текущий статус НЕ равен 200 (в Python "не равно" пишется как `!=`):
#    - Увеличь свой счетчик ошибок на 1.
# 4. В самом конце (без отступов, на уровне `for`) выведи результат на экран с помощью print.
#
# Пиши свой код ниже:
errors_count = 0
for status in statuses:
    if status != 200:
        errors_count = errors_count + 1
print(errors_count)



