# ТВОЯ ЗАДАЧА:
# Написать функцию `get_total_completed_amount(order_list)`.
# Функция должна посчитать и вернуть ОБЩУЮ СУММУ денег (amount) только тех заказов,
# у которых статус равен "completed".
#
# ТВОЙ ПОШАГОВЫЙ ПЛАН (включай логику!):
# 1. Создай переменную-накопитель для суммы, например `total_sum = 0`.
# 2. Напиши цикл `for`, чтобы перебрать список `order_list` по одному заказу (order).
# 3. Внутри цикла напиши `if`: проверь, что статус заказа (`order["status"]`) равен "completed".
# 4. Если равен: прибавь сумму этого заказа (`order["amount"]`) к твоей переменной `total_sum`.
# 5. В самом конце функции (после цикла, сдвинув отступ обратно) верни `total_sum` наружу через `return`.
#
# Пиши код функции ниже:
def full_sum(order_list):
    total_sum = 0  # 1. Создали пустую копилку

    for order in order_list:  # 2. Берем по одному заказу из списка
        if order["status"] == "completed":  # 3. Если статус равен "completed"
            total_sum = total_sum + order["amount"]  # 4. Добавляем сумму заказа в копилку

    return total_sum  # 5. Возвращаем итоговую сумму наружу
# (Раскомментируй код ниже, когда напишешь функцию, чтобы проверить результат):
