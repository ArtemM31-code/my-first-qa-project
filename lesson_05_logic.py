# ТВОЯ НОВАЯ ЛОГИЧЕСКАЯ ЗАДАЧА:
# Вместо того чтобы просто считать ошибки, давай соберем все ошибочные статусы в ОТДЕЛЬНЫЙ список.
# Это нужно, например, чтобы отправить подробный отчет разработчикам.
#
# Список статусов, которые вернул сайт:
statuses = [200, 404, 200, 500, 200, 401]
#
# Твой план действий:
# 1. Создай пустой список для ошибок (например, `failed_statuses = []`).
# 2. Напиши цикл `for`, чтобы перебрать список `statuses`.
# 3. Напиши условие `if`: если текущий статус НЕ равен 200:
#    - Сделай отступ и добавь этот статус в свой пустой список.
#      (Подсказка: чтобы добавить элемент в список, используется метод `.append()`. 
#       Например: `failed_statuses.append(status)`)
# 4. В самом конце (без отступов) выведи свой список ошибок на экран через print().
#
# Пиши свой код ниже:
failed_statuses = []
for status in statuses:
    if status != 200:
        failed_statuses.append(status)
print(failed_statuses)


