# === УРОК 6: БОЕВАЯ QA-ЗАДАЧА НА ЛОГИКУ (Проверка прав доступа из API-ответа) ===
#
# В реальном автотестировании API мы постоянно получаем данные в виде Словарей (Dictionary).
# Ответ сервера (JSON) представляет собой пары "ключ: значение".
#
# Представь: наш автотест сделал запрос к API и получил профиль пользователя:
user_profile = {
    "username": "artem_qa",
    "email": "artem@test.com",
    "is_admin": False,
    "active_tests": 12
}
#
# ТВОЯ ЛОГИЧЕСКАЯ ЗАДАЧА:
# Написать автоматическую проверку прав доступа.
#
# Правило безопасности:
# Доступ в панель управления разрешен ТОЛЬКО если:
# 1. Пользователь является админом (`is_admin` равен True)
# И (используй оператор `and`)
# 2. У него запущено более 10 тестов (`active_tests` больше 10)
#
# План действий:
# 1. Напиши условие `if`. Достань значения из словаря по ключам (например: `user_profile["is_admin"]`).
# 2. Используй `== True` (или просто само значение) и оператор `and` для объединения двух условий.
# 3. Если оба условия выполняются, сделай отступ и выведи: "Доступ к панели управления РАЗРЕШЕН!"
# 4. Напиши блок `else:`, который выведет: "Доступ ЗАБЛОКИРОВАН!"
#
# Пиши свой код ниже:
if user_profile["is_admin"] == True and user_profile["active_tests"] > 10:
    print("Доступ к панели управления РАЗРЕШЕН!")
else:
    print("Доступ ЗАБЛОКИРОВАН!")




